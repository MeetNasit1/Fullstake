/* Javascript Math.Object Method */

/* Static properties */

// Math.PI
{
    let math1=Math.PI
    console.log(math1);
}

// Math.E

{
    let math2 = Math.E
    console.log(math2);
}

// Math.abs()
{
    let math3=Math.abs(10)
    let math4=Math.abs(-10)
    let math5=Math.abs(0)
    console.log(math3);
    console.log(math4);
    console.log(math5);
}

// Math.ceil()
{
    let math6=Math.ceil(10.45)
    let math7=Math.ceil(-10.45)
    console.log(math6);
    console.log(math7);
}

// Math.floor()
{
    let math8=Math.floor(12.56)
    let math9=Math.floor(-12.56)
    console.log(math8);
    console.log(math9);
    
}
// Math.max()
{
    let math10 = Math.max(1,50,60,10,30,78)
    let math11 = Math.min(1,50,60,10,30,78)
    console.log(math10);
    console.log(math11);
}

// Math.pow()
{
    let math12 = Math.pow(5 ,500)
    console.log(math12);   

}
// Math.random()
{
    let math13 = Math.round(Math.random()*1000000000)
    console.log(math13);
}

// Math.round()
{
    let math14 = Math.round(12.45)
    let math15 = Math.round(12.55)
    console.log(math14);
    console.log(math15);

}




